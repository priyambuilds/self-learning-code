#relational operators
a = 10
b = 20
print(a == b) #this means a is equal to b
print(a != b) #this means a is not equal to b
print(a > b) #this means a is greater than b
print(a < b) #this means a is less than b

#logical operators
#and - true if both operands are true else false
# or - true if at least one operand is true else false
# not - inverts true to false and false to true
a = True
b = False
print(a and b) #output is false
print(a or b) #output is true
print(not a) #output is false





