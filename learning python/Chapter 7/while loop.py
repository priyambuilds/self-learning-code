#while loop. while loop is used to repeat a block of code until a condition is met.
i = 1
while(i<6):
    print(i)
    i += 1
#output: 1 2 3 4 5

# Write a program to print the content of a list using while loops.
list = ["apple", "banana", "cherry", 1, False]
i = 0
while(i<len(list)):
    print(list[i])
    i += 1



